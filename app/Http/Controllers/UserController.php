    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user =  User::find($id);
        
        $user->name = $request['nombre'.$id];
        $user->email = $request['email'.$id];
        if($request['password'.$id]){
          $user->password = bcrypt($request['password'.$id]);
        }
        $user->save();

        return redirect('usuario')->with('info','Usuario actualizado');
       /*  return $request->all(); */
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        User::destroy($id);

        return redirect('usuario')->with('success','Usuario eliminado correctamente');
    }
